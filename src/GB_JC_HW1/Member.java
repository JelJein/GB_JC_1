package GB_JC_HW1;

public class Member {
    public String firstName;
    public String lastName;
    public int maxStr;
    public int maxSpeed;
    public int maxJump;

    public Member(String firstName, String lastName, int maxStr, int maxSpeed, int maxJump) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.maxStr = maxStr;
        this.maxSpeed = maxSpeed;
        this.maxJump = maxJump;
    }
}
